fn gen_species(pokered: &Path, charmap: &Charmap) -> String {
    // Internal index -> constant name ($00 NO_MON .. $BE VICTREEBEL, 39 gaps).
    let mons_file = AsmFile::load(pokered, "constants/pokemon_constants.asm");
    let mons = scan_consts(&mons_file, &|l| l.starts_with("DEF NUM_POKEMON_INDEXES"));
    let num_indexes = mons.final_value - 1;
    assert_eq!(num_indexes, 190, "NUM_POKEMON_INDEXES changed");
    assert_eq!(mons.items[0], (0, "NO_MON".to_string()));
    let internal_by_index: BTreeMap<i64, String> =
        mons.items.iter().map(|(v, n)| (*v, n.clone())).collect();

    // DEX_* constant -> dex number (1..=151).
    let dex_file = AsmFile::load(pokered, "constants/pokedex_constants.asm");
    let dex = scan_consts(&dex_file, &|l| l.starts_with("DEF NUM_POKEMON"));
    assert_eq!(dex.final_value - 1, 151, "NUM_POKEMON changed");
    let dex_values = const_map(&dex);

    // PokedexOrder: one db per internal index 1..=190.
    let order_file = AsmFile::load(pokered, "data/pokemon/dex_order.asm");
    let mut order: Vec<u8> = Vec::new();
    for (lineno, code) in order_file.code_lines() {
        let ctx = || order_file.ctx(lineno);
        if code.starts_with("assert_table_length") {
            break;
        }
        let Some(entry) = code.strip_prefix("db ") else {
            continue; // label / table_width
        };
        let entry = entry.trim();
        if entry == "0" {
            order.push(0);
        } else {
            let value = *dex_values
                .get(entry)
                .unwrap_or_else(|| panic!("{}: unknown dex constant {entry:?}", ctx()));
            order.push(u8::try_from(value).expect("dex number fits in u8"));
        }
    }
    assert_eq!(order.len(), 190, "PokedexOrder length changed");

    // Cross-check PokedexOrder against the internal constant list: gaps in
    // one must be gaps in the other, and names must correspond where the
    // internal mon has a real dex entry.
    let mut index_to_dex = [0u8; 256];
    for (i, &dexno) in order.iter().enumerate() {
        let index = i as i64 + 1;
        index_to_dex[index as usize] = dexno;
        match (internal_by_index.get(&index), dexno) {
            (None, 0) => {}
            (None, d) => panic!("internal index ${index:02X} is const_skip but has dex {d}"),
            (Some(_), 0) => {} // named glitch entries (FOSSIL_*, MON_GHOST) have dex 0
            (Some(name), d) => {
                let expected = format!("DEX_{name}");
                assert_eq!(
                    dex_values.get(&expected).copied(),
                    Some(d as i64),
                    "internal {name} (index ${index:02X}) does not map to {expected}"
                );
            }
        }
    }
    let nonzero = index_to_dex.iter().filter(|&&d| d != 0).count();
    assert_eq!(nonzero, 151, "expected 151 valid species");

    let mut dex_to_index = [0u8; 152];
    for (index, &dexno) in index_to_dex.iter().enumerate() {
        if dexno != 0 {
            assert_eq!(dex_to_index[dexno as usize], 0, "dex {dexno} mapped twice");
            dex_to_index[dexno as usize] = index as u8;
        }
    }
    assert!(
        dex_to_index[1..].iter().all(|&i| i != 0),
        "dex map not surjective"
    );

    // MonsterNames: 190 dname entries in internal-index order.
    let names_file = AsmFile::load(pokered, "data/pokemon/names.asm");
    let mut internal_names: Vec<String> = Vec::new();
    for (lineno, code) in names_file.code_lines() {
        if code.starts_with("assert_table_length") {
            break;
        }
        if let Some(rest) = code.strip_prefix("dname ") {
            internal_names.push(quoted(rest, &names_file.ctx(lineno)));
        }
    }
    assert_eq!(internal_names.len(), 190, "MonsterNames length changed");

    let mut species_names = vec![String::new(); 152];
    for dexno in 1..=151usize {
        let index = dex_to_index[dexno] as usize;
        let raw = &internal_names[index - 1];
        species_names[dexno] =
            charmap.normalize(raw, &format!("{} (dex {dexno})", names_file.path.display()));
    }

    let base_stats = parse_base_stats(pokered, &dex_values);

    // ---- emit ----
    let mut out = String::new();
    let w = &mut out;
    let _ = writeln!(w, "//! Species tables, indexed as documented per item.");
    let _ = writeln!(w);
    let _ = writeln!(w, "use crate::gen1::data::BaseStats;");
    let _ = writeln!(w);
    let _ = writeln!(w, "/// Internal species index -> National Dex number");
    let _ = writeln!(
        w,
        "/// (0 for NO_MON, MissingNo and other invalid indexes)."
    );
    emit_u8_array(w, "INDEX_TO_DEX", &index_to_dex);
    let _ = writeln!(w);
    let _ = writeln!(
        w,
        "/// National Dex number (1-151) -> internal species index; `[0]` unused."
    );
    emit_u8_array(w, "DEX_TO_INDEX", &dex_to_index);
    let _ = writeln!(w);
    let _ = writeln!(
        w,
        "/// English species names indexed by National Dex number; `[0]` unused."
    );
    emit_str_array(w, "SPECIES_NAMES", &species_names);
    let _ = writeln!(w);
    let _ = writeln!(
        w,
        "/// Base stats indexed by National Dex number; `[0]` unused (all zero)."
    );
    let _ = writeln!(w, "pub const BASE_STATS: [BaseStats; 152] = [");
    let zero = [0u8; 10];
    let _ = writeln!(w, "    {}", base_stats_literal(&zero));
    for (dexno, stats) in base_stats.iter().enumerate() {
        let _ = writeln!(w, "    // {} {}", dexno + 1, species_names[dexno + 1]);
        let _ = writeln!(w, "    {}", base_stats_literal(stats));
    }
    let _ = writeln!(w, "];");
    out
}

/// Parse all 151 base stats files (dex order; Mew lives apart) into raw
/// `[hp, atk, def, spd, spc, type1, type2, catch, exp, growth]` rows.
fn parse_base_stats(pokered: &Path, dex_values: &BTreeMap<String, i64>) -> Vec<[u8; 10]> {
    let type_values = const_map(&scan_consts(
        &AsmFile::load(pokered, "constants/type_constants.asm"),
        &|l| l.starts_with("DEF NUM_TYPES"),
    ));
    let growth_values = parse_growth_constants(pokered);

    // Include order from base_stats.asm (dex 1..=150) ...
    let list = AsmFile::load(pokered, "data/pokemon/base_stats.asm");
    let mut files: Vec<String> = Vec::new();
    for (lineno, code) in list.code_lines() {
        if let Some(rest) = code.strip_prefix("INCLUDE ") {
            files.push(quoted(rest, &list.ctx(lineno)));
        }
    }
    assert_eq!(files.len(), 150, "base_stats.asm include count changed");
    // ... plus Mew's, included from data/pokemon/mew.asm (dex 151).
    let mew = AsmFile::load(pokered, "data/pokemon/mew.asm");
    let mew_includes: Vec<String> = mew
        .code_lines()
        .filter_map(|(lineno, code)| {
            code.strip_prefix("INCLUDE ")
                .filter(|rest| rest.contains("base_stats"))
                .map(|rest| quoted(rest, &mew.ctx(lineno)))
        })
        .collect();
    assert_eq!(
        mew_includes,
        ["data/pokemon/base_stats/mew.asm"],
        "mew.asm base stats include changed"
    );
    files.extend(mew_includes);

    let mut rows = Vec::new();
    for (i, rel) in files.iter().enumerate() {
        let dexno = i as i64 + 1;
        let file = AsmFile::load(pokered, rel);
        // Collect `db` entries in order; fields are positional (see the
        // commented layout in any base_stats file).
        let mut dbs: Vec<(usize, Vec<String>)> = Vec::new();
        for (lineno, code) in file.code_lines() {
            if let Some(rest) = code.strip_prefix("db ") {
                dbs.push((
                    lineno,
                    rest.split(',').map(|f| f.trim().to_string()).collect(),
                ));
            }
        }
        assert!(
            dbs.len() >= 7,
            "{}: expected at least 7 db lines",
            file.path.display()
        );

        let field = |db: usize, n: usize, want: usize| -> String {
            let (lineno, fields) = &dbs[db];
            assert_eq!(
                fields.len(),
                want,
                "{}: expected {want} comma-separated fields",
                file.ctx(*lineno)
            );
            fields[n].clone()
        };
        let num = |db: usize, n: usize, want: usize| -> u8 {
            let value = parse_num(&field(db, n, want), &file.ctx(dbs[db].0));
            u8::try_from(value).unwrap_or_else(|_| {
                panic!("{}: value {value} out of u8 range", file.ctx(dbs[db].0))
            })
        };

        // db 0: pokedex id — must match this file's position in the list.
        let dex_const = field(0, 0, 1);
        assert_eq!(
            dex_values.get(&dex_const).copied(),
            Some(dexno),
            "{}: dex id {dex_const} does not match include position {dexno}",
            file.path.display()
        );
        // db 1: hp, atk, def, spd, spc
        let stats: Vec<u8> = (0..5).map(|n| num(1, n, 5)).collect();
        // db 2: type1, type2
        let type_of = |n: usize| -> u8 {
            let name = field(2, n, 2);
            let value = *type_values
                .get(&name)
                .unwrap_or_else(|| panic!("{}: unknown type {name:?}", file.ctx(dbs[2].0)));
            u8::try_from(value).expect("type id fits in u8")
        };
        let (type1, type2) = (type_of(0), type_of(1));
        // db 3: catch rate; db 4: base exp yield; db 5: level-1 learnset
        // (skipped); db 6: growth rate.
        let catch_rate = num(3, 0, 1);
        let exp_yield = num(4, 0, 1);
        assert_eq!(
            dbs[5].1.len(),
            4,
            "{}: expected 4 level-1 moves",
            file.ctx(dbs[5].0)
        );
        let growth_name = field(6, 0, 1);
        let growth = *growth_values.get(&growth_name).unwrap_or_else(|| {
            panic!(
                "{}: unknown growth rate {growth_name:?}",
                file.ctx(dbs[6].0)
            )
        });

        rows.push([
            stats[0],
            stats[1],
            stats[2],
            stats[3],
            stats[4],
            type1,
            type2,
            catch_rate,
            exp_yield,
            u8::try_from(growth).expect("growth rate fits in u8"),
        ]);
    }
    rows
}

fn parse_growth_constants(pokered: &Path) -> BTreeMap<String, i64> {
    let file = AsmFile::load(pokered, "constants/pokemon_data_constants.asm");
    let scan = scan_consts(&file, &|l| l.starts_with("DEF NUM_GROWTH_RATES"));
    // The file holds several const blocks; keep only the GROWTH_* one and
    // verify it is the contiguous block ending at NUM_GROWTH_RATES.
    let growth: BTreeMap<String, i64> = scan
        .items
        .iter()
        .filter(|(_, n)| n.starts_with("GROWTH_"))
        .map(|(v, n)| (n.clone(), *v))
        .collect();
    assert_eq!(
        growth.len() as i64,
        scan.final_value,
        "GROWTH_* block not contiguous"
    );
    assert_eq!(growth.get("GROWTH_MEDIUM_FAST"), Some(&0));
    assert_eq!(growth.get("GROWTH_SLOW"), Some(&5));
    growth
}

fn base_stats_literal(row: &[u8; 10]) -> String {
    format!(
        "BaseStats {{ hp: {}, attack: {}, defense: {}, speed: {}, special: {}, \
         type1: {}, type2: {}, catch_rate: {}, exp_yield: {}, growth_rate: {} }},",
        row[0], row[1], row[2], row[3], row[4], row[5], row[6], row[7], row[8], row[9]
    )
}

