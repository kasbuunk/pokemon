fn gen_maps(pokered: &Path) -> String {
    let file = AsmFile::load(pokered, "constants/map_constants.asm");
    let scan = scan_consts(&file, &|l| l.starts_with("DEF NUM_MAPS"));
    let num_maps = scan.final_value;
    assert_eq!(num_maps, 0xF8, "NUM_MAPS changed");
    assert_eq!(scan.items.len() as i64, num_maps, "map ids must be dense");

    let mut names = vec![String::new(); 256];
    for (id, name) in &scan.items {
        names[*id as usize] = name.clone();
    }

    let mut out = String::new();
    let w = &mut out;
    let _ = writeln!(
        w,
        "//! Map constant names indexed by map id (\"\" for unused ids)."
    );
    let _ = writeln!(w);
    emit_str_array(w, "MAP_NAMES", &names);
    out
}

