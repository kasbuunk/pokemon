fn gen_events(pokered: &Path) -> String {
    let file = AsmFile::load(pokered, "constants/event_constants.asm");
    let scan = scan_consts(&file, &|l| l.starts_with("DEF NUM_EVENTS"));
    let num_events = scan.final_value;

    // Pin the shape of the source so silent upstream changes fail generation:
    // 507 named events on sparse bit indexes, NUM_EVENTS = $A00 (wEventFlags
    // is flag_array NUM_EVENTS = 320 bytes).
    assert_eq!(scan.items.len(), 507, "event constant count changed");
    assert_eq!(num_events, 0xA00, "NUM_EVENTS changed");
    let max_bit = scan.items.iter().map(|(v, _)| *v).max().expect("nonempty");
    assert_eq!(max_bit, 2522, "last event bit changed");
    for (bit, name) in &scan.items {
        assert!(
            name.starts_with("EVENT_"),
            "unexpected event constant {name} at bit {bit}"
        );
        assert!(
            (0..num_events).contains(bit),
            "event bit {bit} out of range"
        );
    }

    let mut names = vec![String::new(); num_events as usize];
    for (bit, name) in &scan.items {
        assert!(
            names[*bit as usize].is_empty(),
            "event bit {bit} declared twice"
        );
        names[*bit as usize] = name.clone();
    }

    let mut out = String::new();
    let w = &mut out;
    let _ = writeln!(
        w,
        "//! Event flag names indexed by `wEventFlags` bit number."
    );
    let _ = writeln!(w, "//!");
    let _ = writeln!(
        w,
        "//! pokered allocates event bits per map with gaps; unnamed bits"
    );
    let _ = writeln!(
        w,
        "//! are \"\". 507 bits are named; NUM_EVENTS = $A00 = 2560 bits"
    );
    let _ = writeln!(
        w,
        "//! (wEventFlags spans 320 bytes of WRAM, all copied into the save)."
    );
    let _ = writeln!(w);
    emit_str_array_decl(w, "static", "EVENT_FLAG_NAMES", &names);
    out
}

