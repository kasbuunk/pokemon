fn gen_moves(pokered: &Path, charmap: &Charmap) -> String {
    let const_file = AsmFile::load(pokered, "constants/move_constants.asm");
    let consts = scan_consts(&const_file, &|l| l.starts_with("DEF NUM_ATTACKS"));
    assert_eq!(consts.final_value - 1, 165, "NUM_ATTACKS changed");
    assert_eq!(consts.items[0], (0, "NO_MOVE".to_string()));
    assert_eq!(consts.items.len(), 166, "move ids must be dense 0..=165");

    let type_values = const_map(&scan_consts(
        &AsmFile::load(pokered, "constants/type_constants.asm"),
        &|l| l.starts_with("DEF NUM_TYPES"),
    ));

    let names_file = AsmFile::load(pokered, "data/moves/names.asm");
    let names = parse_li_list(&names_file, charmap);
    assert_eq!(names.len(), 165, "MoveNames length changed");

    let moves_file = AsmFile::load(pokered, "data/moves/moves.asm");
    let mut rows: Vec<(u8, u8, u8, u8)> = Vec::new(); // power, type, accuracy, pp
    for (lineno, code) in moves_file.code_lines() {
        let Some(rest) = code.strip_prefix("move ") else {
            continue; // macro definition, table_width, labels
        };
        let ctx = || moves_file.ctx(lineno);
        let fields: Vec<&str> = rest.split(',').map(str::trim).collect();
        assert_eq!(fields.len(), 6, "{}: expected 6 move fields", ctx());
        let expected_const = &consts.items[rows.len() + 1].1;
        assert_eq!(
            fields[0],
            expected_const,
            "{}: move order diverges from constants/move_constants.asm",
            ctx()
        );
        let power = parse_num(fields[2], &ctx());
        let type_ = *type_values
            .get(fields[3])
            .unwrap_or_else(|| panic!("{}: unknown type {:?}", ctx(), fields[3]));
        let percent = parse_num(fields[4], &ctx());
        assert!(
            (0..=100).contains(&percent),
            "{}: accuracy out of range",
            ctx()
        );
        let accuracy = percent * 255 / 100; // the ROM stores `\5 percent`
        let pp = parse_num(fields[5], &ctx());
        assert!((0..=40).contains(&pp), "{}: PP out of range", ctx());
        rows.push((
            u8::try_from(power).unwrap_or_else(|_| panic!("{}: power out of range", ctx())),
            u8::try_from(type_).expect("type id fits in u8"),
            u8::try_from(accuracy).expect("accuracy fits in u8"),
            u8::try_from(pp).expect("pp fits in u8"),
        ));
    }
    assert_eq!(rows.len(), 165, "Moves table length changed");

    let mut out = String::new();
    let w = &mut out;
    let _ = writeln!(
        w,
        "//! Move table indexed by move id (0 = none, 1-165 real moves)."
    );
    let _ = writeln!(w);
    let _ = writeln!(w, "use crate::gen1::data::MoveInfo;");
    let _ = writeln!(w);
    let _ = writeln!(
        w,
        "/// `accuracy` is the raw ROM byte: `percent * 255 / 100` (100% = 255)."
    );
    let _ = writeln!(w, "pub const MOVES: [MoveInfo; 166] = [");
    let _ = writeln!(
        w,
        "    MoveInfo {{ name: \"\", power: 0, type_: 0, accuracy: 0, pp: 0 }},"
    );
    for (i, (power, type_, accuracy, pp)) in rows.iter().enumerate() {
        let _ = writeln!(
            w,
            "    MoveInfo {{ name: {:?}, power: {power}, type_: {type_}, \
             accuracy: {accuracy}, pp: {pp} }},",
            names[i]
        );
    }
    let _ = writeln!(w, "];");
    out
}

/// Parse a `list_start` / `li "NAME"` list, normalizing each entry through
/// the Gen 1 charmap.
fn parse_li_list(file: &AsmFile, charmap: &Charmap) -> Vec<String> {
    let mut names = Vec::new();
    for (lineno, code) in file.code_lines() {
        // assert_list_length may occur mid-list (e.g. after the regular
        // items, before the elevator floors) — it is not a terminator.
        if let Some(rest) = code.strip_prefix("li ") {
            let ctx = file.ctx(lineno);
            names.push(charmap.normalize(&quoted(rest, &ctx), &ctx));
        }
    }
    names
}

// ---------------------------------------------------------------------------
// items.rs
// ---------------------------------------------------------------------------

fn gen_items(pokered: &Path, charmap: &Charmap) -> String {
    // Regular item + elevator-floor ids: constants up to the `const_next $C4`
    // jump that starts the HM/TM range.
    let const_file = AsmFile::load(pokered, "constants/item_constants.asm");
    let consts = scan_consts(&const_file, &|l| l.starts_with("const_next"));
    assert_eq!(consts.items[0], (0, "NO_ITEM".to_string()));
    assert_eq!(consts.items.len(), 0x62, "item constant count changed");
    assert!(
        consts
            .items
            .iter()
            .enumerate()
            .all(|(i, (v, _))| *v == i as i64),
        "item ids must be dense"
    );

    let names_file = AsmFile::load(pokered, "data/items/names.asm");
    let names = parse_li_list(&names_file, charmap);
    assert_eq!(
        names.len(),
        consts.items.len() - 1,
        "ItemNames length does not match item constants"
    );

    let mut item_names = vec![String::new(); 256];
    item_names[1..=0x61].clone_from_slice(&names);
    // HM01-HM05 ($C4-$C8) and TM01-TM55 ($C9-$FF): ItemNames stops before
    // them; the game prints "TMxx"/"HMxx" from the item id (TM55 = $FF is a
    // glitch TM but still renders this way).
    for (id, name) in item_names.iter_mut().enumerate().take(0xC9).skip(0xC4) {
        *name = format!("HM{:02}", id - 0xC3);
    }
    for (id, name) in item_names.iter_mut().enumerate().skip(0xC9) {
        *name = format!("TM{:02}", id - 0xC8);
    }

    let mut out = String::new();
    let w = &mut out;
    let _ = writeln!(
        w,
        "//! Item names indexed by item id (\"\" for invalid/glitch ids)."
    );
    let _ = writeln!(w, "//!");
    let _ = writeln!(
        w,
        "//! Ids $54-$61 are the elevator floor pseudo-items; $C4-$C8 are"
    );
    let _ = writeln!(
        w,
        "//! HM01-HM05 and $C9-$FF are TM01-TM55, named the way the game"
    );
    let _ = writeln!(w, "//! renders them.");
    let _ = writeln!(w);
    emit_str_array(w, "ITEM_NAMES", &item_names);
    out
}

