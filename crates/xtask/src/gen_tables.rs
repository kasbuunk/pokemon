//! `cargo xtask gen-tables --pokered <dir>`: regenerate the static data
//! tables in `crates/pksave/src/gen1/data/generated/` from a pret/pokered
//! checkout, by plain text parsing of its .asm files.
//!
//! Parsing is deliberately strict: any line that does not match the expected
//! shape panics with file+line context rather than silently producing wrong
//! tables.

use std::collections::BTreeMap;
use std::fmt::Write as _;
use std::fs;
use std::path::{Path, PathBuf};

use crate::charmap::Charmap;
use crate::pins;

pub fn run(pokered: &Path) {
    assert!(
        pokered.join("constants/pokemon_constants.asm").is_file(),
        "{} does not look like a pokered checkout",
        pokered.display()
    );
    let out_dir = Path::new(env!("CARGO_MANIFEST_DIR")).join("../pksave/src/gen1/data/generated");
    fs::create_dir_all(&out_dir).unwrap_or_else(|e| panic!("create {}: {e}", out_dir.display()));

    let charmap = Charmap::load(&pokered.join("constants/charmap.asm"));

    write_generated(&out_dir, "species.rs", gen_species(pokered, &charmap));
    write_generated(&out_dir, "moves.rs", gen_moves(pokered, &charmap));
    write_generated(&out_dir, "items.rs", gen_items(pokered, &charmap));
    write_generated(&out_dir, "types.rs", gen_types(pokered, &charmap));
    write_generated(&out_dir, "events.rs", gen_events(pokered));
    write_generated(&out_dir, "maps.rs", gen_maps(pokered));

    println!("generated tables in {}", out_dir.display());
}

fn write_generated(dir: &Path, name: &str, body: String) {
    let path = dir.join(name);
    let header = format!(
        "//! Generated by `cargo xtask gen-tables` from pret/pokered @ {} — do not edit.\n",
        pins::POKERED_SHA
    );
    fs::write(&path, header + &body).unwrap_or_else(|e| panic!("write {}: {e}", path.display()));
    // Keep the checked-in files identical whether they come fresh from
    // gen-tables or have been through `cargo fmt --all`.
    let status = std::process::Command::new("rustfmt")
        .arg("--edition=2021")
        .arg(&path)
        .status()
        .unwrap_or_else(|e| panic!("run rustfmt on {}: {e}", path.display()));
    assert!(status.success(), "rustfmt failed on {}", path.display());
    println!("wrote {name}");
}

// ---------------------------------------------------------------------------
// Low-level .asm line helpers
// ---------------------------------------------------------------------------

struct AsmFile {
    path: PathBuf,
    lines: Vec<String>,
}

impl AsmFile {
    fn load(pokered: &Path, rel: &str) -> AsmFile {
        let path = pokered.join(rel);
        let text =
            fs::read_to_string(&path).unwrap_or_else(|e| panic!("read {}: {e}", path.display()));
        AsmFile {
            path,
            lines: text.lines().map(str::to_string).collect(),
        }
    }

    fn ctx(&self, lineno: usize) -> String {
        format!("{}:{}", self.path.display(), lineno + 1)
    }

    /// Iterate `(lineno, code)` with comments stripped and blanks skipped.
    fn code_lines(&self) -> impl Iterator<Item = (usize, String)> + '_ {
        self.lines.iter().enumerate().filter_map(|(n, line)| {
            let code = strip_comment(line).trim().to_string();
            (!code.is_empty()).then_some((n, code))
        })
    }
}

/// Strip a `;` comment, ignoring semicolons inside double-quoted strings.
fn strip_comment(line: &str) -> &str {
    let mut in_string = false;
    for (i, c) in line.char_indices() {
        match c {
            '"' => in_string = !in_string,
            ';' if !in_string => return &line[..i],
            _ => {}
        }
    }
    line
}

/// Parse an integer literal: `$1F` (hex), `%101` (binary), or decimal.
fn parse_num(tok: &str, ctx: &str) -> i64 {
    let tok = tok.trim();
    let parsed = if let Some(hex) = tok.strip_prefix('$') {
        i64::from_str_radix(hex, 16)
    } else if let Some(bin) = tok.strip_prefix('%') {
        i64::from_str_radix(bin, 2)
    } else {
        tok.parse()
    };
    parsed.unwrap_or_else(|_| panic!("{ctx}: expected number, got {tok:?}"))
}

/// Evaluate `NUM (('+'|'-') NUM)*`, e.g. `$F0 - 2` in const_next.
fn eval_expr(expr: &str, ctx: &str) -> i64 {
    let mut tokens = expr.split_whitespace();
    let first = tokens
        .next()
        .unwrap_or_else(|| panic!("{ctx}: empty const expression"));
    let mut value = parse_num(first, ctx);
    while let Some(op) = tokens.next() {
        let rhs = tokens
            .next()
            .unwrap_or_else(|| panic!("{ctx}: dangling operator in {expr:?}"));
        match op {
            "+" => value += parse_num(rhs, ctx),
            "-" => value -= parse_num(rhs, ctx),
            _ => panic!("{ctx}: unsupported operator {op:?} in {expr:?}"),
        }
    }
    value
}

/// Extract the string from a `"..."` literal starting at `s`.
fn quoted(s: &str, ctx: &str) -> String {
    let s = s.trim();
    let inner = s
        .strip_prefix('"')
        .unwrap_or_else(|| panic!("{ctx}: expected quoted string, got {s:?}"));
    let end = inner
        .find('"')
        .unwrap_or_else(|| panic!("{ctx}: unterminated string in {s:?}"));
    inner[..end].to_string()
}

// ---------------------------------------------------------------------------
// RGBDS `const_def`/`const`/`const_skip`/`const_next` scanner
// ---------------------------------------------------------------------------

struct ConstScan {
    /// `(value, name)` in declaration order.
    items: Vec<(i64, String)>,
    /// `const_value` at the stop line.
    final_value: i64,
}

/// Scan a constants file. `map_const` (from `constants/map_constants.asm`) is
/// treated as a plain `const` of its first argument. Scanning starts at the
/// first `const_def` (so `const \1` lines inside MACRO bodies are ignored)
/// and ends when `stop` matches (the stop line is not processed).
fn scan_consts(file: &AsmFile, stop: &dyn Fn(&str) -> bool) -> ConstScan {
    let mut items: Vec<(i64, String)> = Vec::new();
    let mut value: i64 = 0;
    let mut started = false;
    let mut stopped = false;

    for (lineno, code) in file.code_lines() {
        let ctx = || file.ctx(lineno);
        if stop(&code) {
            stopped = true;
            break;
        }
        let (word, rest) = match code.split_once(char::is_whitespace) {
            Some((w, r)) => (w, r.trim()),
            None => (code.as_str(), ""),
        };
        if word == "const_def" {
            started = true;
            value = if rest.is_empty() {
                0
            } else {
                eval_expr(rest, &ctx())
            };
            continue;
        }
        if !started {
            continue;
        }
        match word {
            "const" | "map_const" => {
                let name = rest
                    .split([',', ' ', '\t'])
                    .next()
                    .filter(|n| !n.is_empty())
                    .unwrap_or_else(|| panic!("{}: const without a name", ctx()));
                assert!(
                    name.chars().all(|c| c.is_ascii_alphanumeric() || c == '_'),
                    "{}: unexpected const name {name:?} (macro body?)",
                    ctx()
                );
                items.push((value, name.to_string()));
                value += 1;
            }
            "const_skip" => {
                value += if rest.is_empty() {
                    1
                } else {
                    eval_expr(rest, &ctx())
                };
            }
            "const_next" => {
                let next = eval_expr(rest, &ctx());
                assert!(next >= value, "{}: const_next goes backwards", ctx());
                value = next;
            }
            "end_indoor_group" => {} // map_constants bookkeeping, no id consumed
            _ => {}                  // DEF/ASSERT/labels/etc.
        }
    }
    assert!(
        stopped,
        "{}: never reached expected end marker",
        file.path.display()
    );
    ConstScan {
        items,
        final_value: value,
    }
}

fn const_map(scan: &ConstScan) -> BTreeMap<String, i64> {
    scan.items.iter().map(|(v, n)| (n.clone(), *v)).collect()
}

