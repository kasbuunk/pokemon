fn gen_types(pokered: &Path, charmap: &Charmap) -> String {
    let const_file = AsmFile::load(pokered, "constants/type_constants.asm");
    let consts = scan_consts(&const_file, &|l| l.starts_with("DEF NUM_TYPES"));
    let num_types = consts.final_value;
    assert_eq!(num_types, 0x1B, "NUM_TYPES changed");

    // data/types/names.asm: a `dw .Label` per declared type constant, in the
    // same order (the REPT block fills the unused-id gap), then `.Label: db`
    // strings.
    let names_file = AsmFile::load(pokered, "data/types/names.asm");
    let mut dw_labels: Vec<String> = Vec::new();
    let mut strings: BTreeMap<String, String> = BTreeMap::new();
    let mut in_rept = false;
    for (lineno, code) in names_file.code_lines() {
        let ctx = || names_file.ctx(lineno);
        if code.starts_with("REPT") {
            in_rept = true;
        } else if code.starts_with("ENDR") {
            in_rept = false;
        } else if let Some(rest) = code.strip_prefix("dw ") {
            if in_rept {
                continue; // gap filler (points at .Normal)
            }
            let label = rest
                .trim()
                .strip_prefix('.')
                .unwrap_or_else(|| panic!("{}: expected local label in dw", ctx()));
            dw_labels.push(label.to_string());
        } else if let Some(label_rest) = code.strip_prefix('.') {
            let (label, rest) = label_rest
                .split_once(':')
                .unwrap_or_else(|| panic!("{}: expected `.Label:`", ctx()));
            let rest = rest.trim();
            let db = rest
                .strip_prefix("db ")
                .unwrap_or_else(|| panic!("{}: expected db string after label", ctx()));
            let raw = quoted(db, &ctx());
            let raw = raw
                .strip_suffix('@')
                .unwrap_or_else(|| panic!("{}: type name missing @ terminator", ctx()));
            strings.insert(label.to_string(), charmap.normalize(raw, &ctx()));
        }
    }
    assert_eq!(
        dw_labels.len(),
        consts.items.len(),
        "type name pointer count does not match type constants"
    );

    let mut type_names = vec![String::new(); num_types as usize];
    for ((id, const_name), label) in consts.items.iter().zip(&dw_labels) {
        let name = strings
            .get(label)
            .unwrap_or_else(|| panic!("no string for type label .{label}"));
        type_names[*id as usize] = name.clone();
        // Sanity: the label should match the constant (PSYCHIC_TYPE -> Psychic).
        let normalized_const = const_name.replace("_TYPE", "").replace('_', "");
        assert!(
            normalized_const.eq_ignore_ascii_case(label),
            "type constant {const_name} paired with unexpected label .{label}"
        );
    }

    let mut out = String::new();
    let w = &mut out;
    let _ = writeln!(w, "//! Type names indexed by Gen 1 type id.");
    let _ = writeln!(w, "//!");
    let _ = writeln!(
        w,
        "//! Type ids are sparse: $00-$08 are the physical types, $14-$1A the"
    );
    let _ = writeln!(w, "//! special types, and $09-$13 are unused (\"\").");
    let _ = writeln!(w);
    emit_str_array(w, "TYPE_NAMES", &type_names);
    out
}

