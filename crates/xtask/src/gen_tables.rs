// Array emission
// ---------------------------------------------------------------------------

fn emit_u8_array(out: &mut String, name: &str, values: &[u8]) {
    let _ = writeln!(out, "pub const {name}: [u8; {}] = [", values.len());
    for chunk in values.chunks(16) {
        let row: Vec<String> = chunk.iter().map(|v| format!("{v:#04X}")).collect();
        let _ = writeln!(out, "    {},", row.join(", "));
    }
    let _ = writeln!(out, "];");
}

fn emit_str_array(out: &mut String, name: &str, values: &[String]) {
    emit_str_array_decl(out, "const", name, values);
}

/// Like [`emit_str_array`] but lets the caller pick `const`/`static`
/// (clippy's `large_const_arrays` wants big tables as `static`).
fn emit_str_array_decl(out: &mut String, decl: &str, name: &str, values: &[String]) {
    let _ = writeln!(out, "pub {decl} {name}: [&str; {}] = [", values.len());
    let mut i = 0;
    while i < values.len() {
        if values[i].is_empty() {
            // Compact runs of empty entries.
            let start = i;
            while i < values.len() && values[i].is_empty() {
                i += 1;
            }
            let row: Vec<&str> = (start..i).map(|_| "\"\"").collect();
            for chunk in row.chunks(16) {
                let _ = writeln!(out, "    {},", chunk.join(", "));
            }
        } else {
            let _ = writeln!(out, "    {:?}, // {i:#04X}", values[i]);
            i += 1;
        }
    }
    let _ = writeln!(out, "];");
}
